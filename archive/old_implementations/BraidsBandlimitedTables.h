// Bandlimited comb tables for different frequency zones
// These are used to generate alias-free waveforms at different pitches

// Generate bandlimited comb at runtime
inline void GenerateBandlimitedComb(int16_t* output, int zone) {
    const int WAVETABLE_SIZE = 256;
    const double SAMPLE_RATE = 48000.0;
    
    double f0 = 440.0 * std::pow(2.0, (18.0 + 8.0 * zone - 69.0) / 12.0);
    if (zone == 14) {
        f0 = SAMPLE_RATE / 2.0 - 1.0;
    } else {
        f0 = std::min(f0, SAMPLE_RATE / 2.0);
    }
    
    double period = SAMPLE_RATE / f0;
    double m = 2.0 * std::floor(period / 2.0) + 1.0;
    
    // Generate the bandlimited pulse
    for (int j = 0; j < WAVETABLE_SIZE + 1; j++) {
        double i = (j - WAVETABLE_SIZE / 2.0) / WAVETABLE_SIZE;
        double pulse;
        
        if (j == WAVETABLE_SIZE / 2) {
            pulse = 1.0;
        } else {
            double denom = m * std::sin(M_PI * i);
            if (std::abs(denom) < 1e-9) {
                pulse = 1.0;
            } else {
                pulse = std::sin(M_PI * i * m) / denom;
            }
        }
        
        // Apply quadrature rotation
        int index = (j + WAVETABLE_SIZE / 4) % WAVETABLE_SIZE;
        output[index] = static_cast<int16_t>(pulse * 32000.0);
    }
    
    // Copy first sample to end for interpolation
    output[WAVETABLE_SIZE] = output[0];
}


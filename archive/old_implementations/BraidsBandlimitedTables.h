// Initialize all 15 zones of bandlimited combs
extern int16_t bandlimited_comb_tables[15][257];

inline void InitializeBandlimitedTables() {
    for (int zone = 0; zone < 15; zone++) {
        if (zone == 0) {
            // Copy pre-generated zone 0
            for (int i = 0; i < 257; i++) {
                bandlimited_comb_tables[0][i] = bandlimited_comb_0[i];
            }
        } else if (zone == 1) {
            // Copy pre-generated zone 1
            for (int i = 0; i < 257; i++) {
                bandlimited_comb_tables[1][i] = bandlimited_comb_1[i];
            }
        } else if (zone == 2) {
            // Copy pre-generated zone 2
            for (int i = 0; i < 257; i++) {
                bandlimited_comb_tables[2][i] = bandlimited_comb_2[i];
            }
        } else {
            // Generate remaining zones at runtime
            GenerateBandlimitedComb(bandlimited_comb_tables[zone], zone);
        }
    }
}

